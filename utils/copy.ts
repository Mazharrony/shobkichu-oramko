// Number formatting utilities
export function formatPrice(bdt: number): string {
  return `${bdt.toLocaleString("bn-BD")} ${copy.currency}`;
}

export function formatCapacity(liters: number): string {
  return `${liters.toLocaleString("bn-BD")} ${copy.liters}`;
}

export function convertToBanglaNumber(num: number | string): string {
  const bengaliNumbers = ["à§¦", "à§§", "à§¨", "à§©", "à§ª", "à§«", "à§¬", "à§­", "à§®", "à§¯"];
  return String(num).replace(/\d/g, (digit) => bengaliNumbers[parseInt(digit)]);
}
